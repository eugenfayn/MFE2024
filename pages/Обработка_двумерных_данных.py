import streamlit as st

# Заголовок страницы
st.title('Обработка двумерных расчетных данных в программе ParaView')

# Шаг 1: Загрузка данных
st.header('Шаг 1: Загрузка данных')
st.markdown("""
1. В меню "File" выберите "Open" и выберите файл с двумерными расчетными данными.
2. Нажмите кнопку "Apply" или "Ok" для загрузки данных.
""")

# Шаг 2: Визуализация данных
st.header('Шаг 2: Визуализация данных')
st.markdown("""
1. Выберите тип визуализации в меню "Display".
2. Настройте параметры визуализации, такие как цвет, масштаб, контур и т. д.
3. Нажмите кнопку "Apply" для применения изменений.
""")

# Шаг 3: Обработка данных
st.header('Шаг 3: Обработка данных')
st.markdown("""
1. Используйте фильтры в меню "Filters" для обработки данных.
2. Некоторые полезные фильтры для обработки двумерных данных включают:
   - **ExtractEdges**: Извлекает граничные элементы из данных.
   - **Smooth**: Применяет сглаживание к данным для уменьшения шума.
   - **ResampleToImage**: Изменяет разрешение данных.
   - **Slice**: Создает срезы данных вдоль плоскости или линии.
   - **Gradient**: Вычисляет градиенты данных для анализа направления изменения значений.
   - **Threshold**: Классифицирует данные в соответствии с пороговыми значениями.
""")

# Шаг 4: Визуализация обработанных данных
st.header('Шаг 4: Визуализация обработанных данных')
st.markdown("""
1. Повторите шаги для визуализации обработанных данных, как описано в Шаге 2.
2. Настройте параметры визуализации в соответствии с результатами обработки.
""")

# Шаг 5: Экспорт данных (при необходимости)
